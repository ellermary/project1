package StaticVars;

public class NonStaticMethods {
    public double multiply (double a, double b) {
        return a * b;
    }
    public double add (double a, double b) {
        return a + b;
    }
    public double division (double a, double b) {
        return a / b;
    }
    public double subtract (double a, double b) {
        return a - b;
    }
}
